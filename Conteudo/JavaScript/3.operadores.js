// Soma +
var numero1 = 10;
var numero2 = 13;
console.log(numero1 + numero2);

// Subitração -
var numeroUm = 15;
var numeroDois = 7;
console.log(numeroUm - numeroDois);

// Multiplicação *
var numeroA = 10;
var numeroB = 22;
console.log(numeroA * numeroB);

// Divisão /
var numeroC = 10;
var numeroD = 2;
console.log(numeroC / numeroD);

// Módulo %
var umNumero = 10;
var outroNumero = 3;
console.log(umNumero % outroNumero);

// Incremento ++
var primeiroNumero = 10;
console.log(++primeiroNumero);

// Decremento --
var segundoNumero = 10;
console.log(--segundoNumero);

// Maior que >
var aNumero = 10;
var bNumero = 5;
console.log(aNumero > bNumero);

// Menor que <
var cNumero = 2;
var dNumero = 10;
console.log(cNumero < dNumero)

// Maior ou igual >=
var numUm = 10;
var numDois = 10;
console.log(numUm >= numDois);

// Menor ou igual <=
var num1 = 10;
var num2 = 33;
console.log(num1 <= num2);

// Comparação de Igualdade Simples == (Compara apenas valores)
var num3 = 10;
var num4 = "10";
console.log(num3 == num4);

// Comparação de Desigualdade Simples != (Compara apenas valores)
var numTres = 10;
var numQuatro = "15";
console.log(numTres != numQuatro);

// Comparação de Igualdade Rigorosa === (Compara valores e tipos de dados)
var numCinco = 10;
var numSeis = 10;
console.log(numCinco === numSeis);

// Comparação de Desigualdade Rigorosa !== (Compara valores e tipos de dados)
var num5 = 10;
var num6 = "10";
console.log(num5 !== num6);

// E &&
var dia = "Segunda";
var clima = "Ensolarado";
console.log(dia == "Segunda" && clima == "Ensolarado");

// Ou ||
var esporte = "Rugby";
var pessoas = 12;
console.log(esporte == "Futebol" || pessoas >= 10);

// Negação !
var qualquerNum1 = 10;
var qualquerNum2 = 7;
console.log(!qualquerNum1 > qualquerNum2);

// Concatenação " "
var meuNome = "Rodrigo";
var meuSobrenome = "Pegasano";
console.log(meuNome + " " + meuSobrenome);